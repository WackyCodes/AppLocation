    // method for turn on GPS
    public void turnGPSOn(OnGpsListener gpsListener) {
        if (locationManager.isProviderEnabled(LocationManager.GPS_PROVIDER)) {
            if (gpsListener != null) {
                gpsListener.gpsStatus(true);
            }
        } else {
            mSettingsClient
                    .checkLocationSettings(mLocationSettingsRequest)
                    .addOnSuccessListener((Activity) mContext, locationSettingsResponse -> {
                        //  GPS is already enable, callback GPS status through listener
                        if (gpsListener != null) {
                            gpsListener.gpsStatus(true);
                        }
                    })
                    .addOnFailureListener((Activity) mContext, exception -> {
                        int statusCode = ((ApiException) exception).getStatusCode();
                        switch (statusCode) {
                            case LocationSettingsStatusCodes.RESOLUTION_REQUIRED:
                                try {
                                    // Show the dialog by calling startResolutionForResult(), and check the
                                    // result in onActivityResult().
                                    ResolvableApiException rae = (ResolvableApiException) exception;
                                    rae.startResolutionForResult((Activity) mContext, Constants.GPS_REQUEST);
                                } catch (IntentSender.SendIntentException sie) {
                                    Log.i(TAG, "PendingIntent unable to execute request.");
                                }
                                break;
                            case LocationSettingsStatusCodes.SETTINGS_CHANGE_UNAVAILABLE:
                                String errorMessage = "Location settings are inadequate, and cannot be " +
                                        "fixed here. Fix in Settings.";
                                Log.e(TAG, errorMessage);
                                Toast.makeText((Activity) mContext, errorMessage, Toast.LENGTH_LONG).show();
                        }
                    });
        }
    }

